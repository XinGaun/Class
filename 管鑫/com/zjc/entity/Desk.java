package com.zjc.entity;

public class Desk {
	private String deskSize;
	private String status;
	private int ztclassid;
	
	public String getDeskSize() {
		return deskSize;
	}
	public void setDeskSize(String deskSize) {
		this.deskSize = deskSize;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public int getZtclassid() {
		return ztclassid;
	}
	public void setZtclassid(int ztclassid) {
		this.ztclassid = ztclassid;
	}
}
